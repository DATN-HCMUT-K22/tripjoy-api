// ──────────────────────────────────────────────────────────────
// Primitive helpers
// ──────────────────────────────────────────────────────────────

function pick(arr) {
    return arr[Math.floor(Math.random() * arr.length)];
}

function randInt(min, max) {
    return Math.floor(Math.random() * (max - min + 1)) + min;
}

function randStr(len = 8) {
    const chars = 'abcdefghijklmnopqrstuvwxyz0123456789';
    let s = '';
    for (let i = 0; i < len; i++) s += chars[Math.floor(Math.random() * chars.length)];
    return s;
}

// ──────────────────────────────────────────────────────────────
// User
// ──────────────────────────────────────────────────────────────

const ADJ = ['swift', 'brave', 'clever', 'happy', 'lucky', 'mighty', 'wise', 'cool'];
const NOUNS = ['traveler', 'explorer', 'wanderer', 'nomad', 'adventurer'];

export function generateUsername() {
    return `${pick(ADJ)}_${pick(NOUNS)}_${randInt(100, 9999)}`;
}

export function generateEmail(username = null) {
    const user = username || `user_${randStr(8)}`;
    return `${user}_${randStr(4)}@testmail.tripjoy.io`;
}

export function generateUser() {
    const username = generateUsername();
    return {
        username,
        email: generateEmail(username),
        password: 'Test123!@#',
        full_name: `Test ${username.split('_')[0]} User`,
    };
}

