// ──────────────────────────────────────────────────────────────
// Itinerary
// ──────────────────────────────────────────────────────────────

export function generateItineraryPayload(groupId) {
    const start = new Date();
    start.setDate(start.getDate() + randInt(7, 30));
    const end = new Date(start);
    end.setDate(end.getDate() + randInt(3, 10));

    // Format as LocalDateTime: YYYY-MM-DDTHH:mm:ss
    const formatDT = (date) => date.toISOString().split('.')[0];

    return {
        group_id: groupId,
        name: `${pick(DESTINATIONS)} Adventure ${randInt(1, 99)}`,
        description: 'Auto-generated by k6 load test',
        start_date: formatDT(start),
        end_date: formatDT(end),
        people_quantity: randInt(1, 6),
        budget_estimate: randInt(5000000, 30000000),
    };
}

